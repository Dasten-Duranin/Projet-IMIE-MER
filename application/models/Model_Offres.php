        return $this->db->query($sql, array($idOffreModif, $idEntreprise))->row();
    }
    public function domaines_reseau_by_offre($idOffre) {
        $sql = 'SELECT demande.idDomaine,demande.idOffre,domaine.idDomaine,domaine.Domaine,domaine.logoDomaine,domaine.TypeDomaine,domaine.* FROM demande
        LEFT JOIN bdd_mer.domaine ON demande.idDomaine = domaine.idDomaine WHERE idOffre = ? AND TypeDomaine="Reseau"' ;
        return $this->db->query($sql, array($idOffre))->result();
    }
    public function domaines_developpement_by_offre($idOffre) {
